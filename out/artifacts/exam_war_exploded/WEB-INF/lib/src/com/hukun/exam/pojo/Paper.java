package com.hukun.exam.pojo;

public class Paper {
    Integer id;
    Integer examid;
    String title;
    String options_;
    String answer;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getExamid() {
        return examid;
    }

    public void setExamid(Integer examid) {
        this.examid = examid;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getOptions_() {
        return options_;
    }

    public void setOptions_(String options_) {
        this.options_ = options_;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    @Override
    public String toString() {
        return "Paper{" +
                "id=" + id +
                ", examid=" + examid +
                ", title='" + title + '\'' +
                ", options_='" + options_ + '\'' +
                ", answer='" + answer + '\'' +
                '}';
    }
}
